    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);

        String fullname = request.getParameter("fullname");
        String date = request.getParameter("date");

        String full = request.getParameter("full");
        String half = request.getParameter("half");
        String free = request.getParameter("zero");

        String place = (String) request.getSession().getAttribute("mueseum");

        PriceDAO priceDAO = new PriceDAO();
        double price = priceDAO.computePrice(place, Double.parseDouble(full), Double.parseDouble(half));

        request.getSession().setAttribute("name", fullname);
        request.getSession().setAttribute("full", full);
        request.getSession().setAttribute("half", half);
        request.getSession().setAttribute("date", date);
        request.getSession().setAttribute("free", free);
        request.getSession().setAttribute("price", price);

        request.getRequestDispatcher("userConfirmation.jsp").forward(request, response);
    }

