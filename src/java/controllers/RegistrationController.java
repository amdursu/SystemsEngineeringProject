    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String email = request.getParameter("email");
        String firstName = request.getParameter("firstname");
        String lastName = request.getParameter("lastname");
        String password = request.getParameter("password");
        String telephone = request.getParameter("phone");

        UserBean registerBean = new UserBean();

        registerBean.setEmail(email);
        registerBean.setFirstName(firstName);
        registerBean.setLastName(lastName);
        registerBean.setPassword(password);
        registerBean.setPhoneNo(telephone);

        UserDAO user = new UserDAO();

        Boolean userRegistered = false;
        try {
            userRegistered = user.userExists(registerBean);
        } catch (SQLException ex) {
            System.out.println("ERROR: " + ex.getMessage());
        }

        if (userRegistered == false) {
            try {
                user.registerUser(registerBean);
            } catch (ClassNotFoundException ex) {
                System.out.println("ERROR: " + ex.getMessage());
            }
            request.getSession().setAttribute("user", firstName);
            request.getSession().setAttribute("email", email);
            request.getRequestDispatcher("museum.jsp").forward(request, response);

        } else {
            request.setAttribute("errMessage", "USER ALREADY EXISTS");
            request.getRequestDispatcher("signup.jsp").forward(request, response);
        }
    }

